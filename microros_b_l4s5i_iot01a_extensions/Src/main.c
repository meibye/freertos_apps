/**
 * @brief  Transmit the given character on the UART.
 * @note  The used UART is defined 
 * @param  ch : character to be transmitted
 * @retval ch
 */
int __io_putchar(int ch) {
  uint8_t c[1];
  c[0] = ch & 0x00FF;
  if (printf_uart != NULL) {
    HAL_UART_Transmit(printf_uart, &c[0], 1, 10);
  }
  return ch;
}

/**
 * @brief  Write character buffer to .
 * @param  file : file identifier
 * @param  ptr : pointer to character buffer
 * @param  len : length of character buffer
 * @retval len
 */
int _write(int file, char *ptr, int len) {
  int DataIdx;
  for (DataIdx = 0; DataIdx < len; DataIdx++) {
    __io_putchar(*ptr++);
  }
  return len;
}
